def parse_clinical_document(xml_data):
    """
    Parse a CDA (Clinical Document Architecture) XML document.
    VULN-511: XXE via external entity reference in CDA document.
    An attacker can embed:
        <!DOCTYPE cda [<!ENTITY xxe SYSTEM "file:///etc/passwd">]>
    and the entity &xxe; in the document body will be replaced with /etc/passwd contents.

    Attack payloads:
    1. File read: <!ENTITY xxe SYSTEM "file:///etc/passwd">
    2. SSRF:      <!ENTITY xxe SYSTEM "http://169.254.169.254/latest/meta-data/">
    3. DoS:       Billion Laughs attack via nested entity expansion
    """
    if isinstance(xml_data, str):
        xml_data = xml_data.encode('utf-8')

    try:
        # VULN-511: ET.fromstring without defusedxml — external entities may be resolved
        root = ET.fromstring(xml_data)  # Bandit B314
    except ET.ParseError as e:
        logger.error(f"CDA parse error: {e}")
        raise

    namespaces = {'cda': 'urn:hl7-org:v3'}
    patient_data = {}

    try:
        patient_role = root.find('.//cda:patientRole', namespaces)
        if patient_role is not None:
            patient_data['id'] = patient_role.findtext('cda:id', namespaces=namespaces)
            patient = patient_role.find('cda:patient', namespaces)
            if patient is not None:
                patient_data['name'] = patient.findtext('cda:name', namespaces=namespaces)
    except Exception as e:
        logger.warning(f"CDA data extraction error: {e}")

    return {'parsed': True, 'patient_data': patient_data, 'element_count': len(root)}


def parse_fhir_xml(xml_data):
    """
    Parse an HL7 FHIR XML resource.
    VULN-514: Same XXE vulnerability as parse_clinical_document().
    FHIR resources are commonly exchanged between healthcare systems.
    A malicious FHIR resource from a federated system triggers XXE.
    """
    if isinstance(xml_data, str):
        xml_data = xml_data.encode('utf-8')

    # VULN-514: ET.parse without defusedxml protection
    tree = ET.parse(BytesIO(xml_data))  # Bandit B314
    root = tree.getroot()

    resources = []
    for child in root:
        resources.append({
            'tag': child.tag,
            'attribs': dict(child.attrib),
            'text': child.text,
        })

    return {'resource_type': root.tag, 'elements': resources}


def parse_dicom_sr_xml(xml_data):
    """
    Parse a DICOM Structured Report in XML format.
    VULN-515: XXE in DICOM SR parsing — radiology reports injectable.
    DICOM SR documents are generated by imaging equipment and may pass through
    multiple systems before reaching the HMS. A compromised PACS system
    could inject XXE payloads into structured reports.
    """
    if isinstance(xml_data, str):
        xml_data = xml_data.encode('utf-8')

    # VULN-515: DICOM SR parsed without defusedxml
    try:
        root = ET.fromstring(xml_data)  # Bandit B314
    except ET.ParseError as e:
        return {'error': str(e)}

    findings = []
    for elem in root.iter('{http://dicom.nema.org/PS3.19/models/NativeDICOM}DicomAttribute'):
        keyword = elem.get('keyword', '')
        value_elem = elem.find('{http://dicom.nema.org/PS3.19/models/NativeDICOM}Value')
        if value_elem is not None:
            findings.append({'keyword': keyword, 'value': value_elem.text})

    return {'findings': findings}


