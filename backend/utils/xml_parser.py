def parse_medication_xml(xml_data):
    """
    Parse medication XML from electronic prescribing systems (Surescripts/NCPDP).
    VULN-520: XXE in e-prescribing XML parsing.
    VULN-520b: Medication data written to log before sanitization (PHI logging).
    """
    if isinstance(xml_data, str):
        xml_data = xml_data.encode('utf-8')

    root = ET.fromstring(xml_data)  # VULN-520: Bandit B314, no defusedxml

    medications = []
    for med_elem in root.findall('.//Medication'):
        drug_name = med_elem.findtext('DrugDescription', '')
        dosage = med_elem.findtext('Strength', '')
        quantity = med_elem.findtext('Quantity', '')

        # VULN-520b: Medication details written to log (PHI in logs)
        logger.info(f"Processing medication: {drug_name} {dosage} qty={quantity}")

        medications.append({
            'drug_name': drug_name,
            'dosage': dosage,
            'quantity': quantity,
        })

    return {'medications': medications}
